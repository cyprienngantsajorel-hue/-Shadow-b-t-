module.exports = (text) => {
    return text
        .replace(/a/gi, "ᴀ")
        .replace(/b/gi, "ʙ")
        .replace(/c/gi, "ᴄ")
        .replace(/d/gi, "ᴅ")
        .replace(/e/gi, "ᴇ")
        .replace(/f/gi, "ғ")
        .replace(/g/gi, "ɢ")
        .replace(/h/gi, "ʜ")
        .replace(/i/gi, "ɪ")
        .replace(/j/gi, "ᴊ")
}